struct textbox {
    struct chr {
        char val;
        int l = -1, r = -1;
    };
    int ptr = 0;
    vector<chr> chrs;
    
    textbox() {
        chrs.push_back({'\n'}); // line feed character for realism :D
    }
    
    bool move_left() {
        if (chrs[ptr].l == -1) return false;
        ptr = chrs[ptr].l;
        
        return true;
    }
    
    bool move_right() {
        if (chrs[ptr].r == -1) return false;
        ptr = chrs[ptr].r;
        
        return true;
    }
    
    void write(char x) {
        chrs.push_back({x, ptr, chrs[ptr].r});
        chrs[ptr].r = chrs.size() - 1;
        
        move_right();
    }
    
    void bksp() {
        int lp = chrs[ptr].l;
        int rp = chrs[ptr].r;
        
        if (lp != -1) chrs[lp].r = rp;
        if (rp != -1) chrs[rp].l = lp;
        
        move_left();
    }
    
    str get() {
        str res = "";
        int cur = chrs[0].r;
        
        while (cur > 0) {
            res += chrs[cur].val;
            cur = chrs[cur].r;
        }
        
        return res;
    }
};
