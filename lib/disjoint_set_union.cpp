#include <bits/stdc++.h>
using namespace std;

// DSU implementation demonstrated by Kruskal algorithm
// https://vnoi.info/wiki/algo/data-structures/disjoint-set-union.md
// graph

const int N = 1e6;
// dual purpose array
// negative array value = number of nodes under it
// positive array value = parrent of node
struct edge {
    int s, e;
    long long w;
    bool operator < (edge x) {
        return w < x.w;
    };
};
struct DSU {
    vector<int> par;
    
    DSU(int n) : par(n + 1, -1) {}
    
    void clear() {
        fill(par.begin(), par.end(), -1);
    }
    
    int root(int u) {
        if (par[u] < 0) return u;
        return par[u] = root(par[u]);
    }
    
    bool merge(int u, int v) {
        u = root(u); v = root(v);
        if (u == v) return false;
        if (par[u] > par[v]) swap(u, v);
        par[u] += par[v];
        par[v] = u;
        return true;
    }
    
    int cmp_size(int u) {
        return -par[root(u)];
    }
};
///////////////////////////////////////
int main() {
    cin.tie(0) -> sync_with_stdio(0);
    /////////////////
    int n, m;
    cin >> n >> m;
    vector<edge> edges;
    for (int i = 0; i < m; i++) {
        int s, e;
        long long w;
        cin >> s >> e >> w;
        edges.push_back({s, e, w});
    };
    sort(edges.begin(), edges.end());
    DSU dsu(n);
    long long cost = 0;
    for (auto &i : edges) {
        if (dsu.merge(i.s, i.e)) {
            cost += i.w;
        };
    };
    cout << cost;
    return 0;
};
