struct state {
    str s = "";
    int cnt = 0;
    vector<int> prev = {};
};
///////////////////////////////////////
char encode(int a) {
    return a + 'a' - 1;
};
int decode(char a) {
    return a - 'a' + 1;
};
void push_states(state c, int p, deque<state> &call) {
    for (auto &i : {-4, 4, -1, 1}) {
        if (p + i < 0 || p + i > 15) {
            continue;
        };
        state n = c;
        n.s[p] = c.s[p + i];
        n.s[p + i] = c.s[p];
        n.cnt++;
        n.prev.push_back(decode(n.s[p]));
        call.push_front(n);
    };
}
///////////////////////////////////////
int main() {
    // ifstream cin("_input");
    // ofstream cout("_output");
    cin.tie(0) -> sync_with_stdio(0);
    /////////////////
    int inp;
    state s;
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            cin >> inp;
            s.s += encode(inp);
        };
    };
    state e = {"abcdefghijklmno`"};
    deque<state> fq = {s}, bq = {e}; // forward/backwards queue
    unordered_map<str, state> fvis;
    mp_optimize(fvis);
    unordered_map<str, bool> bvis;
    mp_optimize(bvis);
    while (!fq.empty()) {
        // current state (forward)
        state fc = fq.back();
        fq.pop_back();
        // skip visited states
        if (fvis.count(fc.s)) {
            continue;
        };
        // store number of moves to reach state
        fvis[fc.s] = fc;
        // blank position (forward)
        int fp = fc.s.find(encode(0));
        // push new states to queue (forward)
        push_states(fc, fp, fq);
        /////////////////
        state bc = bq.back(); // current state (backwards)
        bq.pop_back();
        // skip visited states
        if (bvis.count(bc.s)) {
            continue;
        };
        bvis[bc.s] = true;
        // if 2 bfs join
        if (fvis.count(bc.s)) {
            cout << bc.cnt + fvis[bc.s].cnt << nl;
            for (auto &i : fvis[bc.s].prev) {
                cout << i << sp;
            };
            for (int i = bc.prev.size() - 1; i >= 0; i--) {
                cout <<  bc.prev[i] << sp;
            };
            break;
        };
        int bp = bc.s.find(encode(0));
        push_states(bc, bp, bq);
    };
    /////////////////
    return 0;
};
