int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ifstream cin("ucode_quanhau.inp");
    //ofstream cout("ucode_quanhau.out");
    int n, k;
    cin >> n >> k;
    int r, c;
    cin >> r >> c;
    vector<int> obst_x(k);
    vector<int> obst_y(k);
    for (int i = 0; i < k; i++) {
        cin >> obst_x[i] >> obst_y[i];
    };
    cout << position_count(n, k, r, c, obst_x, obst_y) << nl;
}
