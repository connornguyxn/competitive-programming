vector<int> dp(100001);
vector<vector<int>> a(100001);
///////////////////////////////////////
int dfs(int s) {
    if (!dp[s]) {
        for (int e : a[s]) {
            dp[e] = dfs(e);
            dp[s] = max(dp[s], dp[e] + 1);
        };
    };
    return dp[s];
};
