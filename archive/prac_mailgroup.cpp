vector<vector<int>> mk;
ii r[LIM];
int len = 0;
int cnt[LIM];

int dfs(int i) {
    cnt[i] = 1;
    r[i].fi = ++len;
    for (auto &v : mk[i]) {
        cnt[i] += dfs(v);
    };
    r[i].se = ++len;
    return cnt[i];
};

///////////////////////////////////////
int main() {
    ifstream cin("_input");
    // ofstream cout("_output");
    cin.tie(0) -> sync_with_stdio(0);
    /////////////////
    int n, q;
    cin >> n >> q;
    mk.resize(n + 1);
    int s;
    for (int i = 2; i <= n; i++) {
        cin >> s;
        mk[s].push_back(i);
    };
    
    dfs(1);
    
    while (q--) {
        int m;
        cin >> m;
        vector<int> a(m);
        
        for (int i = 0; i < m; i++) {
            cin >> a[i];
        };
        
        sort(all(a), [](int a, int b) {
            return r[a] < r[b];
        });

        int mx = r[a[0]].se;
        int res = cnt[a[0]];
        
        for (int i = 1; i < m; i++) {
            if (r[a[i]].se > mx) {
                mx = r[a[i]].se;
                res += cnt[a[i]];
            };
        };
        cout << res << nl;
    };
    /////////////////
    return 0;
};
