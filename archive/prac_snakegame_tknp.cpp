int n, m;
///////////////////////////////////////
int path(int x, int y) {
    int res = INFINITY;
    if (x < 1 || x > n || y < 1 || y > m) {
        return res;
    };
    if (vis[x][y]) {
        return res;
    };
    vis[x][y] = 1;
    for (int i = 0; i < 4; i++) {
        res = max(res, path(x + shfx[i], y + shfy[i]));
    };
    vis[x][y] = 0;
};
